bool VulkanDevice::allocateCommandBuffer(rhi::ICommandBuffer& out) {
    VkCommandBufferAllocateInfo info{};
    info.sType = VK_STRUCTURE_TYPE_COMMAND_BUFFER_ALLOCATE_INFO;
    info.commandPool = impl_->commandPool;
    info.level = VK_COMMAND_BUFFER_LEVEL_PRIMARY;
    info.commandBufferCount = 1;

    VkCommandBuffer cmd;
    if (vkAllocateCommandBuffers(impl_->device, &info, &cmd) != VK_SUCCESS) {
        return false;
    }

    out = VulkanCommandBuffer(impl_->imageIndex, cmd);
    return true;
}

void VulkanDevice::freeCommandBuffer(rhi::ICommandBuffer& buf) {
    VkCommandBuffer& handle = reinterpret_cast<VulkanCommandBuffer&>(buf).get();
    vkFreeCommandBuffers(impl_->device, impl_->commandPool, 1, &handle);
}

bool VulkanDevice::submit(rhi::ICommandBuffer& cb) {
    VkSubmitInfo submit_info{};
    submit_info.sType = VK_STRUCTURE_TYPE_SUBMIT_INFO;
    submit_info.commandBufferCount = 1;
    auto& handle = reinterpret_cast<VulkanCommandBuffer&>(cb).get();
    submit_info.pCommandBuffers = &handle;
    if (vkQueueSubmit(impl_->graphicsQueue, 1, &submit_info, VK_NULL_HANDLE) != VK_SUCCESS) {
        return false;
    }

    VkPresentInfoKHR present_info{};
    present_info.sType = VK_STRUCTURE_TYPE_PRESENT_INFO_KHR;
    VkSwapchainKHR swapChains[] = { impl_->swapChain };
    present_info.swapchainCount = 1;
    present_info.pSwapchains = swapChains;
    present_info.pImageIndices = &impl_->imageIndex;

    VkResult ret = vkQueuePresentKHR(impl_->presentQueue, &present_info);
    if (ret == VK_ERROR_OUT_OF_DATE_KHR || ret == VK_SUBOPTIMAL_KHR || impl_->framebufferResized) {
        impl_->framebufferResized = false;
        impl_->recreateSwapChain();
    }
    else if (ret != VK_SUCCESS) {
        return false;
    }

    return true;
}
